package day07;

public class CastingTheCharacter {

    public static void main(String[] args) {

        char grade = 'B';
        System.out.println(grade);

        int letterInNumber = 'B';
        System.out.println(letterInNumber);

        int letterInNumber2 = 'b';
        System.out.println(letterInNumber2);

        char myFirstChar = 100;
        System.out.println(myFirstChar);

        System.out.println("-------------------------------");
        int letterInNumber3 = 'E';
        int letterInNumber4 = 'R';
        int letterInNumber5 = 'F';
        int letterInNumber6 = 'A';
        int letterInNumber7 = 'N';
        System.out.println(letterInNumber3);
        System.out.println(letterInNumber4);
        System.out.println(letterInNumber5);
        System.out.println(letterInNumber6);
        System.out.println(letterInNumber7);

        System.out.println("--------------------------------");
        char letterA = 'a';
        System.out.println(letterA);
        System.out.println(letterA+1);
        System.out.println(" " + letterA+1);

        System.out.println("--------------------------------");

        System.out.println(7 >= 8);
        System.out.println(17 >= 8);

        System.out.println(5 == 5);
        System.out.println(6 != 10);


    }
}

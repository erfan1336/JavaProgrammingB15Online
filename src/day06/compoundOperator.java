package day06;

public class compoundOperator {

    public static void main(String[] args) {

        int studentOnline = 263;

        // +=, -=, *=, /=, %=, are shorthand operator, compound operator

        studentOnline = studentOnline + 5;

        studentOnline += 5;

        studentOnline -= 3;

        studentOnline *= 2;

        System.out.println("Student online is " + studentOnline);




    }
}
